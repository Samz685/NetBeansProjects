    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        Integer pos_actual = tabla.getSelectedRow();
        
        DefaultTableModel modeloTabla = (DefaultTableModel)tabla.getModel();
        
        txtNombre.setText((String) modeloTabla.getValueAt(pos_actual, COL_NOMBRE));
        txtApellidos.setText((String) modeloTabla.getValueAt(pos_actual, COL_APELLIDOS));        
        //txtCurso.setText((String) modeloTabla.getValueAt(pos_actual, 3));   
        comboCurso.getModel().setSelectedItem((String) modeloTabla.getValueAt(pos_actual, COL_CURSO) );
        
        idAlumnoActual = (Integer) modeloTabla.getValueAt(pos_actual, COL_ID);
    }//GEN-LAST:event_tablaMouseClicked


    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed

        if( formularioVacio() ) return;

        //Integer opcion = JOptionPane.showConfirmDialog(null, "¿Deseas borrar?");
        Integer opcion = JOptionPane.showConfirmDialog(null, "¿Deseas borrar?", 
                "Borrar Alumno",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
        if( opcion == JOptionPane.OK_OPTION) {
            datos.delete(idAlumnoActual);
            actualizarTabla();
            idAlumnoActual = null;
            borrarFormulario();
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if( formularioVacio() ) return;
        
        Alumno alumno = datos.get(idAlumnoActual);

        completarAlumnoDesdeFormulario(alumno);

        datos.update(alumno);

        actualizarTabla();
    }//GEN-LAST:event_btnGuardarActionPerformed



    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed

        if( formularioVacio() ) return;

        Alumno alumno = new Alumno();
        completarAlumnoDesdeFormulario(alumno);

        datos.add(alumno);

        actualizarTabla();

        borrarFormulario();
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void completarAlumnoDesdeFormulario(Alumno alumno) {
        alumno.setNombre( txtNombre.getText() );
        alumno.setApellidos( txtApellidos.getText() );
        alumno.setCurso((String) comboCurso.getModel().getSelectedItem());
    }    
    

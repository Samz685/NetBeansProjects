    private void actualizarTabla() {

        ArrayList<Alumno> alumnos = datos.getAll();
        
        ( (DefaultTableModel) tabla.getModel() ).setRowCount(0);
        
        for(var aa : alumnos) {
            Object fila[] = {
                aa.getId(),
                aa.getNombre(),
                aa.getApellidos(),
                aa.getCurso()
            };
            ( (DefaultTableModel) tabla.getModel() ).addRow(fila);    
        }
    }

